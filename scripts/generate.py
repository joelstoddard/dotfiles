#!/usr/bin/env python3
"""Generate OS-specific install scripts from packages.yaml.

Reads packages.yaml and produces arch.sh, debian-ubuntu.sh, and macos.sh
with category selection support. Generated scripts are committed to git
so install-time has zero Python dependency.
"""

import sys
import textwrap
from dataclasses import dataclass
from pathlib import Path
from typing import Optional

try:
    import yaml
except ImportError:
    print("Error: PyYAML is required. Install with: pip install pyyaml", file=sys.stderr)
    sys.exit(1)

SCRIPT_DIR = Path(__file__).parent
REPO_DIR = SCRIPT_DIR.parent
PACKAGES_YAML = REPO_DIR / "packages.yaml"


@dataclass
class PackageSpec:
    key: str
    category: str
    install_type: str  # default, yay, script, npm, cargo, deb, apt-repo, tap, cask
    pkg_name: str
    url: Optional[str] = None
    setup: Optional[str] = None
    tap: Optional[str] = None
    description: str = ""


def load_packages() -> dict:
    with open(PACKAGES_YAML) as f:
        return yaml.safe_load(f)


def resolve_spec(key: str, category: str, raw, os_key: str) -> Optional[PackageSpec]:
    """Resolve a single package's OS entry into a PackageSpec."""
    if raw is None:
        # ~ or null means default package manager, name = key
        return PackageSpec(key=key, category=category, install_type="default", pkg_name=key)
    if isinstance(raw, str):
        # bare string means default package manager, custom name
        return PackageSpec(key=key, category=category, install_type="default", pkg_name=raw)
    if isinstance(raw, dict):
        install_type = raw.get("type", "default")
        # Normalize platform-specific aliases to "default"
        if install_type == "apt":
            install_type = "default"
        pkg_name = raw.get("name", key)
        url = raw.get("url")
        setup = raw.get("setup")
        tap = raw.get("tap")
        return PackageSpec(
            key=key, category=category, install_type=install_type,
            pkg_name=pkg_name, url=url, setup=setup, tap=tap,
        )
    return None


def resolve_os(data: dict, os_key: str) -> dict[str, list[PackageSpec]]:
    """Resolve all packages for a given OS key. Returns {category: [specs]}."""
    categories: dict[str, list[PackageSpec]] = {}
    for category, packages in data.items():
        specs = []
        for key, pkg_data in packages.items():
            if os_key not in pkg_data:
                continue
            spec = resolve_spec(key, category, pkg_data[os_key], os_key)
            if spec:
                spec.description = pkg_data.get("description", "")
                specs.append(spec)
        if specs:
            categories[category] = specs
    return categories


def func_name(category: str) -> str:
    """Convert category name to bash function name."""
    return "install_" + category.replace("-", "_")


def dedup(items: list[str]) -> list[str]:
    """Remove duplicates while preserving order."""
    seen: set[str] = set()
    result = []
    for item in items:
        if item not in seen:
            seen.add(item)
            result.append(item)
    return result


# ---------------------------------------------------------------------------
# Shared templates
# ---------------------------------------------------------------------------

HEADER = """\
#!/bin/bash
# =============================================================================
# GENERATED FILE — DO NOT EDIT
# Generated by scripts/generate.py from packages.yaml
# Re-generate with: python3 scripts/generate.py (or: make generate)
# =============================================================================
set -euo pipefail
"""

HELPERS = """\
FAILED_PACKAGES=()

log() {
    echo "==> $*"
}

warn() {
    echo "WARNING: $*" >&2
}

try_run() {
    local desc="$1"
    shift
    if ! "$@"; then
        warn "Failed to install: $desc"
        FAILED_PACKAGES+=("$desc")
    fi
}
"""

CATEGORY_SELECTOR = """\
# ---------------------------------------------------------------------------
# Category selection
# ---------------------------------------------------------------------------
ALL_CATEGORIES=({categories})

selected_categories=("${{ALL_CATEGORIES[@]}}")

while [[ $# -gt 0 ]]; do
    case "$1" in
        --include)
            IFS=',' read -ra selected_categories <<< "$2"
            shift 2
            ;;
        --exclude)
            IFS=',' read -ra excluded <<< "$2"
            filtered=()
            for cat in "${{selected_categories[@]}}"; do
                skip=false
                for ex in "${{excluded[@]}}"; do
                    if [[ "$cat" == "$ex" ]]; then
                        skip=true
                        break
                    fi
                done
                if ! $skip; then
                    filtered+=("$cat")
                fi
            done
            selected_categories=("${{filtered[@]}}")
            shift 2
            ;;
        *)
            warn "Unknown argument: $1"
            shift
            ;;
    esac
done

log "Selected categories: ${{selected_categories[*]}}"
"""

MAIN_LOOP = """\
# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------
for cat in "${{selected_categories[@]}}"; do
    case "$cat" in
{cases}
        *)
            warn "Unknown category: $cat"
            ;;
    esac
done
"""

SUMMARY = """\
# ---------------------------------------------------------------------------
# Summary
# ---------------------------------------------------------------------------
echo ""
if [[ ${#FAILED_PACKAGES[@]} -gt 0 ]]; then
    echo "=== Installation completed with errors ==="
    echo "The following packages failed to install:"
    for pkg in "${FAILED_PACKAGES[@]}"; do
        echo "  - $pkg"
    done
    exit 1
else
    echo "=== All packages installed successfully ==="
fi
"""


# ---------------------------------------------------------------------------
# Arch emitter
# ---------------------------------------------------------------------------

def emit_arch(categories: dict[str, list[PackageSpec]]) -> str:
    parts = [HEADER, HELPERS]

    # Prerequisites
    parts.append(textwrap.dedent("""\
        # ---------------------------------------------------------------------------
        # Prerequisites
        # ---------------------------------------------------------------------------
        log "Updating system..."
        sudo pacman -Syu --noconfirm

        if ! command -v yay &> /dev/null; then
            log "Installing yay..."
            sudo pacman -S --noconfirm --needed git base-devel
            tmpdir="$(mktemp -d)"
            git clone https://aur.archlinux.org/yay.git "$tmpdir/yay"
            (cd "$tmpdir/yay" && makepkg -si --noconfirm)
            rm -rf "$tmpdir"
        fi
    """))

    # Category functions
    for category, specs in categories.items():
        fn = func_name(category)
        lines = [f'{fn}() {{', f'    log "Installing {category} packages..."']

        # Phase 0: default (pacman) — batched
        default_pkgs = dedup([s.pkg_name for s in specs if s.install_type == "default"])
        if default_pkgs:
            pkg_list = " \\\n        ".join(default_pkgs)
            lines.append(f'    try_run "{category} (pacman)" sudo pacman -S --noconfirm --needed \\')
            lines.append(f'        {pkg_list}')

        # Phase 2: yay packages
        yay_pkgs = [s for s in specs if s.install_type == "yay"]
        for s in yay_pkgs:
            lines.append(f'    try_run "{s.key}" yay -S --noconfirm --needed {s.pkg_name}')

        # Phase 4: script installs
        script_pkgs = [s for s in specs if s.install_type == "script"]
        for s in script_pkgs:
            lines.extend(_script_install_lines(s, indent=4))

        # Phase 5: toolchain (rust, nvm)
        toolchain_pkgs = [s for s in specs if s.key in ("rust", "nvm") and s.install_type == "script"]
        # Already emitted in script phase above — but we need env sourcing after.
        # Add env sourcing for toolchains only if they appeared
        for s in specs:
            if s.key == "rust" and s.install_type == "script":
                lines.append('    [[ -f "$HOME/.cargo/env" ]] && source "$HOME/.cargo/env"')
            if s.key == "nvm" and s.install_type == "script":
                lines.append('    export NVM_DIR="$HOME/.nvm"')
                lines.append('    [[ -s "$NVM_DIR/nvm.sh" ]] && \\. "$NVM_DIR/nvm.sh"')
                lines.append('    command -v nvm &> /dev/null && nvm install 24')

        # Phase 6: toolchain-dependent (cargo, npm)
        cargo_pkgs = [s for s in specs if s.install_type == "cargo"]
        for s in cargo_pkgs:
            lines.append(f'    try_run "{s.key}" cargo install {s.pkg_name}')

        npm_pkgs = [s for s in specs if s.install_type == "npm"]
        for s in npm_pkgs:
            lines.append(f'    try_run "{s.key}" npm install -g {s.pkg_name}')

        lines.append("}")
        lines.append("")
        parts.append("\n".join(lines))

    # Category selector
    cat_list = " ".join(categories.keys())
    parts.append(CATEGORY_SELECTOR.format(categories=cat_list))

    # Main loop
    cases = ""
    for category in categories:
        fn = func_name(category)
        cases += f'        {category})\n            {fn}\n            ;;\n'
    parts.append(MAIN_LOOP.format(cases=cases.rstrip()))

    parts.append(SUMMARY)
    return "\n".join(parts)


